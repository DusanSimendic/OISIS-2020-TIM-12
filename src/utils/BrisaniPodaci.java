package utils;

public class BrisaniPodaci {
	int ID;
	String ImeLeka;
	String Proizvodjac;
	String SifraLeka;
	int IDKor;
	
	public BrisaniPodaci() {
		
	}

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

	public String getImeLeka() {
		return ImeLeka;
	}

	public void setImeLeka(String imeLeka) {
		ImeLeka = imeLeka;
	}

	public String getProizvodjac() {
		return Proizvodjac;
	}

	public void setProizvodjac(String proizvodjac) {
		Proizvodjac = proizvodjac;
	}

	public String getSifraLeka() {
		return SifraLeka;
	}

	public void setSifraLeka(String sifraLeka) {
		SifraLeka = sifraLeka;
	}

	public int getIDKor() {
		return IDKor;
	}

	public void setIDKor(int iDKor) {
		IDKor = iDKor;
	}
}
