package utils;

public class Lek {
	int IDLeka;
	String SifraLeka;
	String ImeLeka;
	String Proizvodjac;
	String Recept;
	int Cena;
	
	public Lek() {
		
	}

	public int getIDLeka() {
		return IDLeka;
	}

	public void setIDLeka(int iDLeka) {
		IDLeka = iDLeka;
	}

	public String getSifraLeka() {
		return SifraLeka;
	}

	public void setSifraLeka(String sifraLeka) {
		SifraLeka = sifraLeka;
	}

	public String getImeLeka() {
		return ImeLeka;
	}

	public void setImeLeka(String imeLeka) {
		ImeLeka = imeLeka;
	}

	public String getProizvodjac() {
		return Proizvodjac;
	}

	public void setProizvodjac(String proizvodjac) {
		Proizvodjac = proizvodjac;
	}

	public String getRecept() {
		return Recept;
	}

	public void setRecept(String recept) {
		Recept = recept;
	}

	public int getCena() {
		return Cena;
	}

	public void setCena(int cena) {
		Cena = cena;
	}
}
